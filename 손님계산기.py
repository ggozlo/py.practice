from random import *
cnt = 0 
for i in range(1,51): # 1~ 50 수 생성(승객)
    time = randrange(5,51) # 5~50 소요 시간 
    if 5<= time <= 15:
        print("[0] {}번째 손님 (소요시간 : {}분)".format(i,time))
        cnt += 1
    else:
        print("[ ] {}번째 손님 (소요시간 : {}분)".format(i,time))
print("총 탑승 승객 : ",cnt)